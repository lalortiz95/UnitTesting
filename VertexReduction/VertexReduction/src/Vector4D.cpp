	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	float CVector4D::Magnitud(const CVector4D & V)
	{
		return sqrt(pow(V.X, 2) + pow(V.Y, 2) + pow(V.Z, 2) + pow(V.W, 2));
	}
	/************************************************************************/
	/*                                                                      */
	/************************************************************************/
	CVector4D CVector4D::Normalize(const CVector4D & V)
	{
		return V / Magnitud(V);
	}

