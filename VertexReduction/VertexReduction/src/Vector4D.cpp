	float CVector4D::Dot(const CVector4D& B)
	{
		float escalar = this->X * B.X + this->Y * B.Y + this->Z * B.Z + this->W * B.W;
		if (escalar <= DELTA && escalar >= 0 - DELTA)
		{
			escalar = 0;
		}
		return escalar;
	}

	CVector4D CVector4D::Cross(const CVector4D & V, const CVector4D & B)
	{
		return CVector4D((V.Y*B.Z - V.Z * B.Y), (V.Z * B.X - V.X * B.Z), (V.X * B.Y - V.Y * B.X), 0);
	}
