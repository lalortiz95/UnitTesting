	/************************************************************************/
	bool CVector4D::Equals(const CVector4D& V, float Tolerance) const
	{
		return abs(X - V.X) < Tolerance && abs(Y - V.Y) < Tolerance && abs(Z - V.Z) < Tolerance && abs(W - V.W) < Tolerance;
	}

